    private void discardTreasuresActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_discardTreasuresActionPerformed
        // TODO add your handling code here:
        // NECESITO SABER DE DONDE QUIERO DESCARTAR
        ArrayList<NapakalakiGame.Treasure> selDiscardVisible = getSelectedTreasures(visibleTreasures);
        ArrayList<NapakalakiGame.Treasure> selDiscardHidden = getSelectedTreasures(hiddenTreasures);
        if (selDiscardVisible.size() == 0) {
            napakalakiModel.discardHiddenTreasures(selDiscardHidden);
        } else if (selDiscardHidden.size() == 0) {
            napakalakiModel.discardVisibleTreasures(selDiscardVisible);
        }
        setPlayer(napakalakiModel.getCurrentPlayer());
    }//GEN-LAST:event_discardTreasuresActionPerformed

